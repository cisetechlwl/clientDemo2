package com.sdb.netool.conf;

public class BankServer {
    String host;
    int port;

    public BankServer() {

    }

    public BankServer(String host, int port) {
        this.host = host;
        this.port = port;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

}
